// Omni first-boot is slow: 3 stage engine cores + multimodal weight load +
// per-kernel Triton JIT for the gfx target. Give generous headroom.
static constexpr long VLLM_OMNI_READY_TIMEOUT_SECONDS = 1800;

// --- ROCm startup shim (mirrors the vllm backend) ---------------------------
// Disables vLLM's pynvml probe so it does not activate the CUDA platform plugin
// on hybrid AMD/NVIDIA hosts. ROCm PyTorch exposes HIP devices via
// torch.cuda/torch.accelerator, so no CUDA_VISIBLE_DEVICES is set.

static std::string with_prepended_pythonpath(const fs::path& dir) {
    const char separator = ':';
    const char* current = std::getenv("PYTHONPATH");
    return dir.string() + ((current && current[0] != '\0')
        ? std::string(1, separator) + current
        : "");
}

static fs::path create_omni_rocm_shim_dir() {
    fs::path runtime_base = path_from_utf8(get_runtime_dir());
    std::random_device rd;
    std::uniform_int_distribution<unsigned int> dis(0, 0xFFFFFF);

    std::error_code ec;
    for (int attempt = 0; attempt < 8; ++attempt) {
        auto nonce = static_cast<unsigned long long>(
            std::chrono::steady_clock::now().time_since_epoch().count());
        std::ostringstream suffix;
        suffix << "vllm-omni-rocm-shim-" << nonce << "-" << std::hex << dis(rd);
        fs::path candidate = runtime_base / suffix.str();
        ec.clear();
        if (fs::create_directory(candidate, ec)) {
            return candidate;
        }
    }
    throw std::runtime_error("Failed to create temporary directory for vLLM-Omni ROCm startup shim");
}

static fs::path write_omni_rocm_startup_shim() {
    fs::path dir = create_omni_rocm_shim_dir();
    fs::path file = dir / "sitecustomize.py";

    std::ofstream out(file, std::ios::trunc);
    if (!out.is_open()) {
        std::error_code ec;
        fs::remove_all(dir, ec);
        throw std::runtime_error("Unable to write vLLM-Omni ROCm startup shim: " + file.string());
    }

    out << R"PY(# Auto-generated by Lemonade for the vLLM-Omni ROCm backend.
import sys

try:
    import vllm.utils.import_utils as _vllm_import_utils

    def _disabled_import_pynvml():
        raise ModuleNotFoundError(
            "vLLM NVML probe disabled by Lemonade for the ROCm backend"
        )

    _vllm_import_utils.import_pynvml = _disabled_import_pynvml
except (ImportError, AttributeError) as exc:
    print(
        f"Lemonade warning: unable to disable vLLM NVML probe: {exc}",
        file=sys.stderr,
    )
)PY";

    if (!out.good()) {
        std::error_code ec;
        fs::remove_all(dir, ec);
        throw std::runtime_error("Failed to write vLLM-Omni ROCm startup shim: " + file.string());
    }
    return dir;
}

static void cleanup_omni_rocm_shim_dir(fs::path& shim_dir) {
    if (shim_dir.empty()) {
        return;
    }
    std::error_code ec;
    fs::remove_all(shim_dir, ec);
    if (ec) {
        LOG(WARNING, "vLLM-Omni") << "Failed to remove ROCm startup shim directory: "
                                  << shim_dir << " (" << ec.message() << ")" << std::endl;
    }
    shim_dir.clear();
}

static void configure_omni_rocm_env(
    const std::string& backend,
    std::vector<std::pair<std::string, std::string>>& env_vars,
    fs::path& shim_dir) {
    if (backend != "rocm") {
        return;
    }
    cleanup_omni_rocm_shim_dir(shim_dir);
    shim_dir = write_omni_rocm_startup_shim();
    env_vars.push_back({"PYTHONPATH", with_prepended_pythonpath(shim_dir)});
}

